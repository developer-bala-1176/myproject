package com.corejava.methods;
/**
 * Abstract class:
 *        The method that does not has method body is known as abstract method.
 * It always declares in the abstract class.
 * It means the class itself must be abstract if it has abstract method. 
 * To create an abstract method, use the keyword abstract.
 */
public abstract class Demo_Ab { // abstract class
	abstract void show();// abstract method declaration.

}
