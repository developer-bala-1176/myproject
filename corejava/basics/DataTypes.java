package com.corejava.basics;

public class DataTypes {
	public static void main(String args[]) {
	int a=23;
    short b=110;
    long c=987654324567l;// l - mention then long type data. 
    double d=23.2323244d;// d - mention then double type data. 
    float e=0.12f;//f- mention then float type data.
    char f='Z';// '' - mention the single word in the inside of the ''.
    String g="Welcome to our coding world!!";// ""- mention the sentance in the inside of the "".
    boolean h=true; //only 2 type  boolean values are available- true , false.
    byte i=28;
    System.out.println("Integer :" +a); // + is act as concodinate. it marge the alphabet + numaric & alphabet + alphabet. 
    System.out.println("Short :" +b);
    System.out.println("Long :" +c);
    System.out.println("Double :" +d);
    System.out.println("Float :" +e);
    System.out.println("Char :" +f);
    System.out.print("Boolean :" +h);
    System.out.print(" String :" +g);
    System.out.println("Byte :"+i);
    // print() - Print statement in the horizondal.
    // println()-Print statement in the vertical.
}

}
